package week2;

public class Calculator
{
	int add(int x, int y)
	{
		int sum=x+y;
		return sum;
	}
	int add(int x, int y,int z)
	{
		int sum=x+y+z;
		return sum;
	}
	double add(double x, double y)
	{
		double sum=x+y;
		return sum;
	}
	double add(double x,double y,double z)
	{
		double sum=x+y+z;
		return sum;
	}
}
